const AnnounceRestrict = {
  MIN: 1,
  MAX: 5,
};

const {
  getRandomInt,
  shuffleArray
} = require(`../../utils.js`);

const fs = require(`fs`);

const getCustomeDate = (count) => {
  const date = new Date();
  date.setDate(date.getDate() + getRandomInt(0, count));
  return date.toLocaleString();
};

const getFullText = (arr) => {
  const copyArr = shuffleArray(arr).slice(0);
  const textSize = getRandomInt(AnnounceRestrict.MIN, arr.length - 1);

  const newArr = new Array(textSize).fill(``).map(() => (copyArr[getRandomInt(0, arr.length - 1)]));

  return newArr;
};

const generateOffers = (count) => (
  Array(count).fill({}).map(() => ({
    title: TITLES[getRandomInt(0, TITLES.length - 1)],
    createdDate: getCustomeDate(-90),
    announce: shuffleArray(SENTENCES).slice(0, getRandomInt(AnnounceRestrict.MIN, AnnounceRestrict.MAX)).join(` `),
    fullText: getFullText(SENTENCES).join(` `),
    category: shuffleArray(CATEGORIES).slice(0, getRandomInt(1, CATEGORIES.length - 1)).join(`, `),
  }))
);

module.exports = {
  name: `--generate`,
  run(args) {
    const [count] = args;
    const countOffer = Number.parseInt(count, 10) || DEFAULT_COUNT;

    const content = JSON.stringify(generateOffers(countOffer));
    fs.writeFile(FILE_NAME, content, (err) => {
      if (countOffer > 1000) {
        return console.info(`Не больше 1000 публикаций.`);
      } else if (err) {
        return console.error(`Can't write data to file...`);
      }

      return console.info(`Operation success. File created.`);
    });
  }
};
